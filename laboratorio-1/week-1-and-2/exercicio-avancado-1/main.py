def main():
    a = float(input("Digite um numero decimal... "))
    b = int(input("Digite um numero inteiro... "))
    
    print(f"A multiplicado por B eh: {a * b}")
    print(f"A dividido por B eh: {a / b}")
    print(f"A mais B eh: {a + b}, e A menos B eh {a - b}")
    print(f"A elevado a B eh: {a ** b}")


if __name__ == "__main__":
    main()